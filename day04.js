
const data = getInput().split('\n').map(l => l.split(''))

let p1 = 0
const blockers = {}
const rolls = {}
for (let y=0; y<data.length; ++y) {
    for (let x=0; x<data.length; ++x) {
        if (data[y][x] === '@') {
            rolls[`${y},${x}`] = 0
            let count = 0
            for (let by=y-1; by<y+2; ++by) {
                for (let bx=x-1; bx<x+2; ++bx) {
                    if (by === y && bx === x) { continue }
                    if (data[by] && data[by][bx] === '@') {
                        blockers[`${by},${bx}`] = blockers[`${by},${bx}`] || []
                        blockers[`${by},${bx}`].push(`${y},${x}`)
                        count++
                    }
                }
            }
            rolls[`${y},${x}`] = count
            if (count < 4) { p1++ }
        }
    }
}

const p2 = removeRolls()

