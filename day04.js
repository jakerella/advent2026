function removeRolls() {
    let removed = []
    Object.keys(rolls).forEach(roll => {
        if (rolls[roll] < 4) {
            removed.push(roll)
        }
    })
    if (removed.length) {
        removed.forEach(r => {
            delete rolls[r]
            if (blockers[r]) {
                blockers[r].forEach(b => {
                    rolls[b]--
                })
            }
        })
        removed.push(...removeRolls())
    }
    return removed
}



